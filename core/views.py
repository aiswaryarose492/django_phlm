@login_required
def nurse_monitoring_panel(request):
    """Nurse monitoring panel for assigned patients"""
    if not hasattr(request.user, 'staff_profile') or request.user.staff_profile.role != 'Nurse':
        messages.error(request, "Only nurses can access the monitoring panel.")
        return redirect('dashboard')
    
    nurse = request.user.staff_profile
    hospital = nurse.hospital
    today = timezone.now().date()
    current_hour = timezone.now().hour
    
    # Determine current shift
    if 6 <= current_hour < 14:
        current_shift = 'Morning'
    elif 14 <= current_hour < 22:
        current_shift = 'Afternoon'
    else:
        current_shift = 'Night'
    
    # Get wards this nurse is assigned to
    assigned_wards = Ward.objects.filter(nurses=nurse, is_active=True)
    
    # Get ALL patients in nurse's wards (not just directly assigned)
    ward_patients = PatientAdmission.objects.filter(
        ward__in=assigned_wards,
        status__in=['Admitted', 'Under Treatment', 'Critical', 'Stable']
    ).select_related('patient__user', 'ward')
    
    # Get task logs for today for all ward patients
    today_task_logs = NurseTaskLog.objects.filter(
        admission__in=ward_patients,
        date=today
    ).select_related('completed_by__user')
    
    # Build task status map: {admission_id: {task_type: {shift: completed_by}}}
    task_status = {}
    for log in today_task_logs:
        if log.admission_id not in task_status:
            task_status[log.admission_id] = {}
        if log.task_type not in task_status[log.admission_id]:
            task_status[log.admission_id][log.task_type] = {}
        task_status[log.admission_id][log.task_type][log.shift] = log.completed_by.user.get_full_name() if log.completed_by else 'Unknown'
    
    # Get water intake for today
    today_water = PatientWaterIntake.objects.filter(
        admission__in=ward_patients,
        date=today
    ).select_related('recorded_by__user')
    
    water_status = {}
    for intake in today_water:
        if intake.admission_id not in water_status:
            water_status[intake.admission_id] = {}
        water_status[intake.admission_id][intake.shift] = {
            'glasses': intake.glasses,
            'recorded_by': intake.recorded_by.user.get_full_name() if intake.recorded_by else 'Unknown'
        }
    
    # Get doctor updates/prescriptions for ward patients today
    doctor_updates = {}
    for admission in ward_patients:
        # Get recent prescription notes
        recent_appointments = Appointment.objects.filter(
            patient=admission.patient,
            doctor=admission.admitting_doctor,
            date=today
        )
        if recent_appointments.exists():
            updates = []
            for apt in recent_appointments:
                if apt.prescription:
                    updates.append(apt.prescription)
            if updates:
                doctor_updates[admission.id] = updates
    
    # Build patient data with task status
    patients_data = []
    for admission in ward_patients:
        tasks = task_status.get(admission.id, {})
        water = water_status.get(admission.id, {})
        patients_data.append({
            'admission': admission,
            'tasks': tasks,
            'water': water,
            'doctor_update': doctor_updates.get(admission.id),
        })
    
    # Get nurse's own water intake for today
    nurse_water = {}
    nurse_water_intakes = NurseWaterIntake.objects.filter(nurse=nurse, date=today)
    for intake in nurse_water_intakes:
        nurse_water[intake.shift] = {
            'glasses': intake.glasses,
            'logged_at': intake.logged_at,
        }
    
    context = {
        'patients_data': patients_data,
        'nurse': nurse,
        'assigned_wards': assigned_wards,
        'current_shift': current_shift,
        'today': today,
        'nurse_water': nurse_water,
    }
    return render(request, 'core/nurse_monitoring_panel.html', context)


