@login_required
def admit_patient(request):
    """Mark patient as serious and admit them"""
    if not request.user.is_doctor:
        messages.error(request, "Only doctors can admit patients.")
        return redirect('dashboard')
    
    if request.method != 'POST':
        return redirect('doctor_dashboard')
    
    try:
        doctor = request.user.doctor_profile
        hospital = doctor.hospital
        
        patient_id = request.POST.get('patient_id')
        ward_id = request.POST.get('ward_id')
        bed_number = request.POST.get('bed_number')
        admission_reason = request.POST.get('admission_reason')
        is_serious = request.POST.get('is_serious') == 'on'
        
        patient = get_object_or_404(Patient, id=patient_id, hospital=hospital)
        ward = get_object_or_404(Ward, id=ward_id, hospital=hospital)
        
        # Check bed availability
        if ward.available_beds <= 0:
            messages.error(request, "No beds available in selected ward.")
            return redirect('doctor_dashboard')
        
        # Create admission
        admission = PatientAdmission.objects.create(
            patient=patient,
            hospital=hospital,
            ward=ward,
            bed_number=bed_number,
            admission_reason=admission_reason,
            admitting_doctor=doctor,
            is_serious=is_serious,
            status='Critical' if is_serious else 'Admitted'
        )
        
        # Update ward occupancy
        ward.occupied_beds += 1
        ward.save()
        
        # Auto-assign nurses from the ward's assigned nurses
        ward_nurses = ward.nurses.filter(user__is_active=True)
        
        # If no nurses assigned to the ward, fallback to least busy hospital nurses
        if not ward_nurses.exists():
            ward_nurses = Staff.objects.filter(
                hospital=hospital,
                role='Nurse',
                user__is_active=True
            ).annotate(
                patient_count=models.Count('assigned_patients')
            ).order_by('patient_count')[:4]
        
        for nurse in ward_nurses:
            admission.assigned_nurses.add(nurse)
            # Update workload
            workload, _ = NurseWorkload.objects.get_or_create(
                nurse=nurse,
                defaults={'max_patients': 5, 'active_patients': 0}
            )
            workload.active_patients += 1
            workload.save()
            
            # Notify nurse
            Notification.objects.create(
                recipient=nurse.user,
                message=f"New patient assigned: {patient.user.get_full_name()} in {ward.ward_number}",
                type='General',
                link='/staff_dashboard/'
            )
        
        # Create notification for patient
        Notification.objects.create(
            recipient=patient.user,
            message=f"You have been admitted to {ward.ward_number}. Bed: {bed_number}. Please report to the ward.",
            type='General'
        )
        
        messages.success(request, f"Patient {patient.user.get_full_name()} admitted successfully to {ward.ward_number}!")
        return redirect('doctor_appointments')
        
    except Exception as e:
        messages.error(request, f"Error admitting patient: {str(e)}")
        return redirect('doctor_appointments')


@login_required
def discharge_patient(request, admission_id):
    """Discharge an admitted patient"""
    if not request.user.is_doctor:
        messages.error(request, "Only doctors can discharge patients.")
        return redirect('dashboard')
    
    admission = get_object_or_404(PatientAdmission, id=admission_id)
    doctor = request.user.doctor_profile
    
    if admission.hospital != doctor.hospital:
        messages.error(request, "You can only discharge patients from your hospital.")
        return redirect('dashboard')
    
    if request.method == 'POST':
        # Create discharge summary
        DischargeSummary.objects.create(
            admission=admission,
            discharged_by=doctor,
            admission_diagnosis=request.POST.get('admission_diagnosis'),
            final_diagnosis=request.POST.get('final_diagnosis'),
            treatment_given=request.POST.get('treatment_given'),
            surgery_procedures=request.POST.get('surgery_procedures', ''),
            condition_at_discharge=request.POST.get('condition_at_discharge'),
            follow_up_instructions=request.POST.get('follow_up_instructions'),
            medications_on_discharge=request.POST.get('medications_on_discharge'),
            activity_restrictions=request.POST.get('activity_restrictions', ''),
            diet_instructions=request.POST.get('diet_instructions', ''),
            warning_signs=request.POST.get('warning_signs', ''),
            follow_up_date=request.POST.get('follow_up_date') or None
        )
        
        # Update admission
        admission.status = 'Discharged'
        admission.discharge_date = timezone.now()
        admission.save()
        
        # Free up bed
        if admission.ward:
            admission.ward.occupied_beds = max(0, admission.ward.occupied_beds - 1)
            admission.ward.save()
        
        # Update nurse workloads
        for nurse in admission.assigned_nurses.all():
            workload, _ = NurseWorkload.objects.get_or_create(nurse=nurse)
            workload.active_patients = max(0, workload.active_patients - 1)
            workload.save()
        
        messages.success(request, f"Patient {admission.patient.user.get_full_name()} discharged successfully!")
        return redirect('doctor_patients')
    
    context = {
        'admission': admission,
        'patient': admission.patient,
    }
    return render(request, 'core/discharge_patient.html', context)


