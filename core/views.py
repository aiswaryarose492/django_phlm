# Extend the confirm_appointment view to use auto-generated credentials
# This helper function can be called when creating new users

def create_user_with_credentials(first_name, last_name, email, phone, user_type='patient', hospital=None):
    """Create a new user with auto-generated credentials and send email"""
    from django.db import transaction
    
    with transaction.atomic():
        # Generate credentials
        username = generate_username(first_name, last_name)
        password = generate_password()
        
        # Create user
        user = User.objects.create_user(
            username=username,
            email=email,
            first_name=first_name,
            last_name=last_name
        )
        user.set_password(password)
        
        # Set user type
        if user_type == 'patient':
            user.is_patient = True
        elif user_type == 'doctor':
            user.is_doctor = True
        elif user_type == 'staff':
            user.is_staff_member = True
        
        user.save()
        
        # Create auto-credentials record
        AutoGeneratedCredentials.objects.create(
            user=user,
            generated_username=username,
            generated_password=password,  # In production, store encrypted
            sent_to_email=email
        )
        
        # Create patient/doctor/staff profile
        if user_type == 'patient' and hospital:
            Patient.objects.create(
                user=user,
                hospital=hospital,
                phone=phone
            )
        
        # Send email
        credentials_sent = send_credentials_email(user, username, password, user_type)
        
        if credentials_sent:
            creds = AutoGeneratedCredentials.objects.get(user=user)
            creds.credentials_sent = True
            creds.sent_at = timezone.now()
            creds.save()
        
        return user, username, password
