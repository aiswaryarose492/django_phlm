@login_required
def manage_wards(request):
    """Manage hospital wards/rooms"""
    # Get hospital based on user type
    if request.user.is_hospital_admin:
        hospital = request.user.hospital_profile
    elif request.user.is_doctor:
        hospital = request.user.doctor_profile.hospital
    elif hasattr(request.user, 'staff_profile'):
        hospital = request.user.staff_profile.hospital
    else:
        messages.error(request, "You don't have permission to access ward management.")
        return redirect('dashboard')
    
    if request.method == 'POST':
        action = request.POST.get('action')
        
        if action == 'add':
            try:
                ward_number = request.POST.get('ward_number', '').strip()
                ward_type = request.POST.get('ward_type', 'General')
                floor = request.POST.get('floor', '1')
                total_beds = int(request.POST.get('total_beds', 10))
                
                if not ward_number:
                    messages.error(request, "Ward number is required.")
                else:
                    Ward.objects.create(
                        hospital=hospital,
                        ward_number=ward_number,
                        ward_type=ward_type,
                        floor=floor,
                        total_beds=total_beds
                    )
                    messages.success(request, f"Ward '{ward_number}' added successfully!")
            except ValueError:
                messages.error(request, "Invalid bed count. Please enter a valid number.")
            except Exception as e:
                messages.error(request, f"Error adding ward: {str(e)}")
                
        elif action == 'edit':
            ward_id = request.POST.get('ward_id')
            try:
                ward = Ward.objects.get(id=ward_id, hospital=hospital)
                ward.ward_number = request.POST.get('ward_number')
                ward.ward_type = request.POST.get('ward_type')
                ward.floor = request.POST.get('floor')
                ward.total_beds = int(request.POST.get('total_beds'))
                ward.save()
                messages.success(request, "Ward updated successfully!")
            except Ward.DoesNotExist:
                messages.error(request, "Ward not found.")
            except Exception as e:
                messages.error(request, f"Error updating ward: {str(e)}")
                
        elif action == 'delete':
            ward_id = request.POST.get('ward_id')
            try:
                ward = Ward.objects.get(id=ward_id, hospital=hospital)
                ward_number = ward.ward_number
                ward.delete()
                messages.success(request, f"Ward '{ward_number}' deleted successfully!")
            except Ward.DoesNotExist:
                messages.error(request, "Ward not found.")
            except Exception as e:
                messages.error(request, f"Error deleting ward: {str(e)}")
        
        elif action == 'assign_nurses':
            ward_id = request.POST.get('ward_id')
            try:
                ward = Ward.objects.get(id=ward_id, hospital=hospital)
                nurse_ids = request.POST.getlist('nurse_ids')
                nurses = Staff.objects.filter(id__in=nurse_ids, hospital=hospital, role='Nurse')
                ward.nurses.set(nurses)
                messages.success(request, f"Nurses assigned to {ward.ward_number} successfully!")
            except Ward.DoesNotExist:
                messages.error(request, "Ward not found.")
            except Exception as e:
                messages.error(request, f"Error assigning nurses: {str(e)}")
        
        return redirect('manage_wards')
    
    wards = Ward.objects.filter(hospital=hospital).prefetch_related('nurses__user').order_by('ward_number')
    available_nurses = Staff.objects.filter(hospital=hospital, role='Nurse', user__is_active=True).select_related('user')
    
    context = {
        'wards': wards,
        'ward_types': Ward.WARD_TYPES,
        'available_nurses': available_nurses,
    }
    return render(request, 'core/manage_wards.html', context)


