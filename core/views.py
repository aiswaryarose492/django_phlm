def select_slot(request, doctor_id):
    """Step 3: Select date and time slot (Public Access)"""
    doctor = get_object_or_404(Doctor, id=doctor_id)
    
    # Check if patient details exist in session (unless logged in patient)
    if not request.user.is_authenticated and 'patient_details' not in request.session:
        messages.warning(request, "Please provide your details first.")
        return redirect('book_patient_details', doctor_id=doctor_id)
    
    # Get next 14 days for available slots
    from datetime import date
    today = date.today()
    available_dates = []
    
    for i in range(1, 15):
        check_date = today + timedelta(days=i)
        day_name = check_date.strftime('%A')
        
        # Check if doctor works on this day
        if doctor.available_days:
            available_days_list = [d.strip() for d in doctor.available_days.split(',')]
            if day_name not in available_days_list:
                continue
        
        # Check if not fully booked
        if doctor.max_appointments_per_day:
            existing_count = Appointment.objects.filter(doctor=doctor, date=check_date).exclude(status='Cancelled').count()
            if existing_count >= doctor.max_appointments_per_day:
                continue
        
        available_dates.append(check_date)
    
    # Generate booked slots dictionary
    booked_slots = {}
    for d in available_dates:
        booked = Appointment.objects.filter(doctor=doctor, date=d).exclude(status='Cancelled').values_list('time', flat=True)
        booked_slots[d.strftime('%Y-%m-%d')] = [t.strftime('%I:%M %p') for t in booked]

    import json
    booked_slots_json = json.dumps(booked_slots)

    # Generate time slots based on doctor's working hours
    time_slots = []
    if doctor.available_start_time and doctor.available_end_time:
        from datetime import time
        start = doctor.available_start_time
        end = doctor.available_end_time
        break_start = doctor.break_start_time
        break_end = doctor.break_end_time
        
        current = start
        while current < end:
            # Check if current time falls within break time
            is_break_time = False
            if break_start and break_end:
                if break_start <= current < break_end:
                    is_break_time = True
            
            if not is_break_time:
                time_slots.append(current.strftime('%I:%M %p'))
                
            # Add 30 minutes interval
            hour, minute = current.hour, current.minute
            if minute == 0:
                current = time(hour, 30)
            else:
                current = time(hour + 1, 0) if hour < 23 else None
                
            if current is None:
                break
                if current is None:
                    break
    else:
        # Default time slots
        default_times = ['09:00 AM', '09:30 AM', '10:00 AM', '10:30 AM', '11:00 AM', '11:30 AM', '02:00 PM', '02:30 PM', '03:00 PM', '03:30 PM', '04:00 PM', '04:30 PM']
        time_slots = default_times
    
    return render(request, 'core/select_slot.html', {
        'doctor': doctor,
        'available_dates': available_dates,
        'time_slots': time_slots,
        'booked_slots': booked_slots_json
    })


