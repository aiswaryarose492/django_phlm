    if hasattr(user, 'doctor_profile'):
        staff_profile = user.doctor_profile
        hospital = staff_profile.hospital
        role = f"Doctor - {staff_profile.specialty}"
    elif hasattr(user, 'staff_profile'):
        staff_profile = user.staff_profile
        hospital = staff_profile.hospital
        role = staff_profile.role
    elif hasattr(user, 'lab_profile'):
        staff_profile = user.lab_profile
        hospital = staff_profile.hospital
        role = "Lab Technician"
    elif hasattr(user, 'pharmacy_profile'):
        staff_profile = user.pharmacy_profile
        hospital = staff_profile.hospital
        role = "Pharmacist"
    else:
        messages.error(request, "Only hospital staff can access this page.")
        return redirect('dashboard')
        
    from django.utils import timezone
    from datetime import timedelta
    
    today = timezone.now().date()
    start_date = today - timedelta(days=30)
    
    attendance_records = list(StaffHealthStatus.objects.filter(
        worker=user, 
        date__gte=start_date,
        date__lte=today
    ).order_by('-date'))
    
    from .models import WorkLog
    
    for record in attendance_records:
        hours = 0
        if role.startswith('Doctor') and staff_profile:
            worklogs = WorkLog.objects.filter(doctor=staff_profile, date=record.date)
            hours = sum(wl.duration() for wl in worklogs)
        elif record.is_on_duty and record.shift_start:
            delta = timezone.now() - record.shift_start
            hours = delta.total_seconds() / 3600
        record.recorded_hours = round(hours, 1) if hours > 0 else "-"
    
    total_days = (today - start_date).days + 1
    days_present = sum(1 for r in attendance_records if r.is_present)
    days_absent = total_days - days_present
    
    base_salary = getattr(staff_profile, 'salary', 0)
    salary_frequency = getattr(staff_profile, 'salary_frequency', 'Monthly')
    deduction_rate = hospital.extra_leave_deduction if hospital else 0
    
    estimated_deduction = days_absent * float(deduction_rate)
    estimated_salary = max(0.0, float(base_salary) - estimated_deduction)
    
    context = {
        'staff_user': user,
        'role': role,
        'staff_profile': staff_profile,
        'hospital': hospital,
        'attendance_records': attendance_records,
        'days_present': days_present,
        'days_absent': days_absent,
        'total_days': total_days,
        'base_salary': base_salary,
        'salary_frequency': salary_frequency,
        'estimated_deduction': estimated_deduction,
        'estimated_salary': estimated_salary,
        'period_days': 30,
    }
    return render(request, 'core/my_attendance.html', context)


