        if ward_id:
            try:
                ward = Ward.objects.get(id=ward_id, hospital=hospital)
                enhanced.ward = ward
                enhanced.save()
            except Ward.DoesNotExist:
                pass
        
        # Auto-assign specialist based on emergency type
        specialist = enhanced.auto_assign_specialist()
        if specialist:
            case.assigned_to = specialist.user
            case.status = 'In Progress'
            case.save()
        
        # Auto-assign at least 4 nurses
        assigned_nurse_count = enhanced.auto_assign_nurses()
        
