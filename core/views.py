    """Allow patients to create their own medication reminders"""
    if not request.user.is_patient:
        return JsonResponse({'success': False, 'error': 'Only patients can create reminders'})
    
    from django.http import JsonResponse
    from datetime import time
    
    if request.method == 'POST':
        try:
            import json
            data = json.loads(request.body)
            
            message = data.get('message', '').strip()
            reminder_time = data.get('time', '')
            reminder_type = data.get('type', 'Medicine')
            
            if not message:
                return JsonResponse({'success': False, 'error': 'Medicine name is required'})
            
            if not reminder_time:
                return JsonResponse({'success': False, 'error': 'Time is required'})
            
            # Parse time string to time object
            try:
                time_obj = time.fromisoformat(reminder_time)
            except ValueError:
                # Try alternative format
                time_parts = reminder_time.split(':')
                if len(time_parts) >= 2:
                    hour = int(time_parts[0])
                    minute = int(time_parts[1])
                    time_obj = time(hour, minute)
                else:
                    return JsonResponse({'success': False, 'error': 'Invalid time format'})
            
            # Create the reminder
            reminder = Reminder.objects.create(
                patient=request.user.patient_profile,
                type=reminder_type,
                message=message,
                time=time_obj,
                is_active=True
            )
            
            return JsonResponse({
                'success': True,
                'reminder': {
                    'id': reminder.id,
                    'message': reminder.message,
                    'time': reminder.time.strftime('%H:%M'),
                    'type': reminder.type
                }
            })
        except Exception as e:
            return JsonResponse({'success': False, 'error': str(e)})
    
    return JsonResponse({'success': False, 'error': 'Invalid request method'})

