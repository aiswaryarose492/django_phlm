@csrf_exempt
def send_otp(request):
    """Send OTP to email for verification"""
    if request.method == 'POST':
        import json
        try:
            data = json.loads(request.body)
            email = data.get('email', '')
            
            # Validate email
            if not email or '@' not in email:
                return JsonResponse({'success': False, 'message': 'Invalid email address'})
            
            # Generate 6-digit OTP
            otp = str(random.randint(100000, 999999))
            
            # Store OTP in session
            request.session['otp'] = otp
            request.session['otp_email'] = email
            request.session['otp_time'] = str(datetime.now())
            
            # Send OTP via email
            subject = 'Your Verification OTP'
            message = f'Your One Time Password (OTP) for appointment booking is: {otp}'
            
            # For testing/logging
            print(f"OTP for {email}: {otp}")
            
            send_mail(
                subject,
                message,
                settings.DEFAULT_FROM_EMAIL,
                [email],
                fail_silently=False,
            )
            
            return JsonResponse({
                'success': True, 
                'message': 'OTP sent successfully to your email!'
            })
        except Exception as e:
            # Return error message to user with guidance
            print(f"Failed to send email OTP: {e}")
            return JsonResponse({
                'success': False, 
                'message': f'Failed to send OTP email: {str(e)}. Please contact support or check SMTP configuration in settings.py.'
            }, status=500)
    
    return JsonResponse({'success': False, 'message': 'Invalid request'})

@csrf_exempt
def verify_otp(request):
    """Verify OTP entered by user"""
    if request.method == 'POST':
        import json
        try:
            data = json.loads(request.body)
            email = data.get('email', '')
            otp = data.get('otp', '')
            
            # Get stored OTP
            stored_otp = request.session.get('otp')
            stored_email = request.session.get('otp_email')
            
            if not stored_otp or not stored_email:
                return JsonResponse({'success': False, 'message': 'OTP expired or not requested'})
            
            # Check if OTP matches
            if otp == stored_otp and email == stored_email:
                # Mark email as verified in session
                request.session['email_verified'] = True
                request.session['verified_email'] = email
                
                # Update user's email in database if logged in
                if request.user.is_authenticated:
                    try:
                        request.user.email = email
                        request.user.save()
                    except Exception:
                        pass
                
                return JsonResponse({'success': True, 'message': 'Email verified successfully!'})
            else:
                return JsonResponse({'success': False, 'message': 'Invalid OTP'})
                
        except Exception as e:
            return JsonResponse({'success': False, 'message': str(e)})
    
    return JsonResponse({'success': False, 'message': 'Invalid request'})

