@login_required
def process_prescription(request, prescription_id):
    if not request.user.is_pharmacy:
        return redirect('dashboard')
        
    if request.method == 'POST':
        prescription = get_object_or_404(Prescription, id=prescription_id)
        
        # Handle Availability
        import json
        availability_data = request.POST.get('availability_data', '{}')
        if availability_data and prescription.medicines:
            try:
                availability = json.loads(availability_data)
                meds = json.loads(prescription.medicines)
                if isinstance(meds, list):
                    for i, med in enumerate(meds):
                        if str(i) in availability:
                            med['is_available'] = availability[str(i)]
                    prescription.medicines = json.dumps(meds)
            except Exception as e:
                pass
        
        # Add Reminder for Patient
        medicine_names = request.POST.getlist('medicine_name[]')
        times = request.POST.getlist('time[]')
        instructions = request.POST.getlist('instruction[]')
        
        for i in range(len(medicine_names)):
            if medicine_names[i] and times[i]:
                # Create Reminder
                Reminder.objects.create(
                    patient=prescription.appointment.patient,
                    type='Medicine',
                    message=f"Take {medicine_names[i]} - {instructions[i]}",
                    time=times[i],
                    is_active=True
                )
        
        # Mark as Dispensed
        prescription.is_dispensed = True
        prescription.save()
        
        messages.success(request, f"Prescription processed for {prescription.appointment.patient.user.get_full_name()}")
        
    return redirect('pharmacy_dashboard')

