@login_required
def hospital_doctors(request):
    if not request.user.is_hospital_admin:
        return redirect('dashboard')
    
    hospital = request.user.hospital_profile
    query = request.GET.get('q')
    doctors = Doctor.objects.filter(hospital=hospital, user__is_active=True)
    
    if query:
        doctors = doctors.filter(
            Q(user__first_name__icontains=query) | 
            Q(user__last_name__icontains=query) |
            Q(specialty__icontains=query) |
            Q(department__icontains=query)
        )
        
    doctors = doctors.order_by('department', 'user__first_name')
    
    # Enhanced Data Preparation
    from .models import WorkLog, LeaveRequest
    from django.utils import timezone
    today = timezone.now().date()
    
    # Group by department
    departments = {}
    for doc in doctors:
        # 1. Check On Duty Status (Doctors use WorkLog)
        doc.is_on_duty = WorkLog.objects.filter(doctor=doc, date=today, end_time__isnull=True).exists()
        
        # 2. Check Leave Status
        doc.current_leave = LeaveRequest.objects.filter(
            doctor=doc,
            start_date__lte=today,
            end_date__gte=today,
            status='Approved'
        ).first()
        
        # 3. Fetch Recent Leaves for History
        doc.recent_leaves = LeaveRequest.objects.filter(doctor=doc).order_by('-start_date')[:5]

        dept = doc.department or 'General'
        if dept not in departments:
            departments[dept] = []
        departments[dept].append(doc)
        
    context = {
        'hospital': hospital,
        'departments': departments,
    }
    return render(request, 'core/hospital_doctors.html', context)

