def create_emergency_case(request):
    if request.method != 'POST':
        return redirect('dashboard')
        
    try:
        from .models import Doctor, Staff, Notification, EmergencyCase
        
        hospital = None
        if request.user.is_hospital_admin:
            hospital = request.user.hospital_profile
        else:
            # Try to infer hospital from other roles
            if hasattr(request.user, 'doctor_profile'):
                hospital = request.user.doctor_profile.hospital
            elif hasattr(request.user, 'staff_profile'):
               hospital = request.user.staff_profile.hospital
        
        if not hospital:
            messages.error(request, "Hospital not identified.")
            return redirect('dashboard')
            
        patient_name = request.POST.get('patient_name')
        symptoms = request.POST.get('symptoms')
        severity = request.POST.get('severity')
        assigned_to_id = request.POST.get('assigned_to')
        
        if not patient_name or not severity:
             messages.error(request, "Please fill in all required fields.")
             return redirect('dashboard')
        
        assigned_user = None
        
        if assigned_to_id:
            try:
                assigned_user = User.objects.get(id=assigned_to_id)
            except User.DoesNotExist:
                pass
        
        # Smart Assignment Logic if not manually assigned
        if not assigned_user:
            from django.utils import timezone
            from django.db.models import Q
            today = timezone.now().date()
            
            # 1. Identify Specialty
            target_specialty = None
            symptoms_lower = symptoms.lower()
            if 'heart' in symptoms_lower or 'chest' in symptoms_lower or 'cardio' in symptoms_lower:
                target_specialty = 'Cardiology'
            elif 'neuro' in symptoms_lower or 'brain' in symptoms_lower or 'head' in symptoms_lower:
                target_specialty = 'Neurosurgery'
            elif 'bone' in symptoms_lower or 'fracture' in symptoms_lower:
                target_specialty = 'Orthopedics'
                
            # 2. Find Available Doctor
            # Filter doctors in this hospital
            doctors = Doctor.objects.filter(hospital=hospital)
            
            candidate = None
            
            # Check for specialty match first
            if target_specialty:
                img_docs = doctors.filter(specialty__icontains=target_specialty)
                for doc in img_docs:
                    # Check if on duty (Open WorkLog)
                    on_duty = WorkLog.objects.filter(doctor=doc, end_time__isnull=True, date=today).exists()
                    if on_duty:
                        candidate = doc.user
                        break
            
            # If no specialty match on duty, find ANY doctor on duty
            if not candidate:
                for doc in doctors:
                    on_duty = WorkLog.objects.filter(doctor=doc, end_time__isnull=True, date=today).exists()
                    if on_duty:
                        candidate = doc.user
                        break
                        
            # 3. If no doctor, try finding a Nurse on duty
            if not candidate:
                nurses = Staff.objects.filter(hospital=hospital, role='Nurse')
                for nurse in nurses:
                    # Check StaffHealthStatus for on_duty
                    try:
                        status = StaffHealthStatus.objects.get(worker=nurse.user, date=today)
                        if status.is_on_duty:
                            candidate = nurse.user
                            break
                    except:
                        pass
                        
            assigned_user = candidate
            
            # Identify Support Team (for notification/info)
            support_team = []
            # Find available Nurses (excluding assigned)
            available_nurses = Staff.objects.filter(hospital=hospital, role='Nurse').exclude(user=assigned_user)
            if available_nurses.exists():
                 support_team.append(f"{available_nurses.count()} Nurses")
            
            # Find Lab/Pharmacy if needed
            if 'blood' in symptoms_lower or 'test' in symptoms_lower:
                 lab_techs = User.objects.filter(lab_profile__hospital=hospital)
                 if lab_techs.exists():
                     support_team.append("Lab Team")

        # Create Case
        case = EmergencyCase.objects.create(
            hospital=hospital,
            patient_name=patient_name,
            symptoms=symptoms,
            severity=severity
        )
        
        # --- Automated Assignment & Notifications ---
        from django.core.mail import send_mail
        from django.conf import settings
        
        # 1. Identify relevant staff (Doctors & Nurses in the same hospital)
        doctors = Doctor.objects.filter(hospital=hospital, available=True, user__is_active=True)
        nurses = Staff.objects.filter(hospital=hospital, role='Nurse', user__is_active=True)
        
        recipients = list(doctors) + list(nurses)
        
        # 2. Automated Assignment (Assign to first available doctor if exists)
        if not case.assigned_to and doctors.exists():
            case.assigned_to = doctors.first().user
            case.status = 'In Progress'
            case.save()
            
        # 3. Create Notifications & Send Alerts
        alert_message = f"URGENT: New Emergency Case! Patient: {patient_name}, Severity: {severity}, Symptoms: {symptoms}"
        
        for staff in recipients:
            # DB Notification
            Notification.objects.create(
                recipient=staff.user,
                message=alert_message,
                type='Emergency',
                link='/hospital/emergency/' # Redirect to emergency list
            )
            
            # Email Notification
            if staff.user.email:
                try:
                    send_mail(
                        subject=f"EMERGENCY ALERT: {severity} Case @ {hospital.name}",
                        message=alert_message,
                        from_email=settings.DEFAULT_FROM_EMAIL,
                        recipient_list=[staff.user.email],
                        fail_silently=True
                    )
                except Exception:
                    pass # Fail silently for demo
            
            # Mock SMS (Log to console)
            print(f"[{timezone.now()}] SMS SENT to {staff.user.username} (Mock): {alert_message}")

        messages.success(request, "Emergency case created and staff notified!")
        return redirect('hospital_emergency')
        
        if not patient_name or not severity:
            print("DEBUG: Missing fields")
            messages.error(request, "Please fill in all required fields.")
            return redirect('dashboard')
            
        print("DEBUG: Fields validated")

        # ... (rest of logic)

    except Exception as e:
        messages.error(request, f"Error creating case: {str(e)}")
        
    return redirect(request.META.get('HTTP_REFERER', 'dashboard'))

