def add_prescription(request, appointment_id):
    if not getattr(request.user, 'is_doctor', False) or request.method != 'POST':
        return redirect('dashboard')
        
    doctor = request.user.doctor_profile
    appointment = get_object_or_404(Appointment, id=appointment_id, doctor=doctor)
    
    medicines = request.POST.get('medicines')
    notes = request.POST.get('notes', '')
    test_names = request.POST.getlist('test_name')
    test_reason = request.POST.get('test_reason', '')
    
    has_medicines = medicines and medicines.strip() and medicines != "No Medicines Prescribed"
    has_tests = test_names and any(t.strip() for t in test_names)
    
    if has_medicines or notes:
        prescription, created = Prescription.objects.get_or_create(appointment=appointment)
        prescription.medicines = medicines if medicines else ''
        prescription.notes = notes
        prescription.save()
        
        messages.success(request, f"Prescription saved for {appointment.patient.user.get_full_name()}")
    
    # Create lab test requests if any
    if has_tests:
        for test_name in test_names:
            if test_name.strip():
                TestRequest.objects.create(
                    appointment=appointment,
                    test_name=test_name.strip(),
                    reason=test_reason
                )
        messages.success(request, f"Lab test(s) requested for {appointment.patient.user.get_full_name()}")
    
    appointment.status = 'Completed'
    appointment.save()
    
    # Redirect based on what was prescribed
    if has_medicines and has_tests:
        messages.info(request, "Both medicines and tests prescribed. Pharmacy and Lab notified.")
    elif has_medicines:
        messages.info(request, "Medicines prescribed. Pharmacy will process.")
    elif has_tests:
        messages.info(request, "Tests prescribed. Lab will process.")
    
    return redirect(request.META.get('HTTP_REFERER', 'doctor_appointments'))

